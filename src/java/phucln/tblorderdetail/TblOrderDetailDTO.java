package phucln.tblorderdetail;

import java.io.Serializable;

/**
 *
 * @author ASUS
 */
public class TblOrderDetailDTO implements Serializable {

    private String orderID;
    private String bookID;
    private String bookTitle;
    private int quantity;
    private float price;

    public TblOrderDetailDTO() {
    }

    public TblOrderDetailDTO(String orderID, String bookID, String bookTitle, int quantity, float price) {
        this.orderID = orderID;
        this.bookID = bookID;
        this.bookTitle = bookTitle;
        this.quantity = quantity;
        this.price = price;
    }

    /**
     * @return the orderID
     */
    public String getOrderID() {
        return orderID;
    }

    /**
     * @param orderID the orderID to set
     */
    public void setOrderID(String orderID) {
        this.orderID = orderID;
    }

    /**
     * @return the bookID
     */
    public String getBookID() {
        return bookID;
    }

    /**
     * @param bookID the bookID to set
     */
    public void setBookID(String bookID) {
        this.bookID = bookID;
    }

    /**
     * @return the bookTitle
     */
    public String getBookTitle() {
        return bookTitle;
    }

    /**
     * @param bookTitle the bookTitle to set
     */
    public void setBookTitle(String bookTitle) {
        this.bookTitle = bookTitle;
    }

    /**
     * @return the quantity
     */
    public int getQuantity() {
        return quantity;
    }

    /**
     * @param quantity the quantity to set
     */
    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    /**
     * @return the price
     */
    public float getPrice() {
        return price;
    }

    /**
     * @param price the price to set
     */
    public void setPrice(float price) {
        this.price = price;
    }

}
